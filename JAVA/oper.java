public class oper{
    public static void main (String[] args) {
       //additon
       int a = 4;
       int b = a+a;
       System.out.println(b);

       //modulus
       int c = 10;
       int d = 5%c;
       System.out.println(d);

       //&& AND
       System.out.println(64>2 && 64>6);

       // || OR
       System.out.println(64<2 || 64<2);

       // ! not equal to
       System.out.println(65!=64);
    }
}